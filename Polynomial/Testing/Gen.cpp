mt19937 rng(chrono::steady_clock::now().time_since_epoch().count());

double random(double l, double r){
	return uniform_real_distribution<double>(l,r)(rng);
}

double f(double x, vector<double> &a){
	double ans = 0.0;
	for(int i = a.size()-1; i >= 0; i--){
		ans *= x;
		ans += a[i];
	}
	return ans;
}

bool found(double &X, vector<double> &v){
	for(auto Y : v){
		if(fabs(X - Y) < 0.1) return true;
	}
	return false;
}

int main(int argc, char* argv[]){
	int n = random(0, 8);
	ofstream out("out1");
	vector<double> a(n + 1);
	for(int i=0; i<=n; i++){
		a[i] = random(-1e2, 1e2);
	}
	for(int i=n; i>=0; i--){
		out << setprecision(10) << fixed << a[i] << '\n';
	}
	printf("%d\n", n + 1);
	vector<double> usedX;
	for(int i=0; i<=n; i++){
		double X = random(-100, 100);
		while(found(X, usedX)) X = random(-100, 100);
		usedX.emplace_back(X);
		printf("%.9lf %.9lf\n", X, f(X, a));
	}
	return 0;
}
